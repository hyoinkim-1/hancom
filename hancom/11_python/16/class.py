# STEP 16 : 클래스 (class)
# 붕어빵 틀로 같은 모양을 여러 개 찍어내듯, class 는 비슷한 정보를 가진
# 여러 개를 같은 틀로 찍어내는 설계도.
# 정보(속성)와 그 정보로 할 수 있는 일(메서드)을 한 통에 함께 묶는 것이 핵심.

# 핵심 단어 (붕어빵 비유):
# - 클래스(class Dog) : 붕어빵 틀 (설계도)
# - 객체(dog1, dog2)  : 틀로 찍어낸 진짜 붕어빵 (실제 만들어진 것)
# - 속성(self.name)   : 붕어빵의 맛·크기 (정보를 담는 변수)
# - 메서드(bark)      : 붕어빵이 할 수 있는 일 (클래스 안의 함수)
# - __init__          : 처음 만들 때 자동 실행되는 준비 단계 (생성자)
# - self              : 지금 이 붕어빵 (자기 자신)

class Dog:                            # 강아지 설계도(틀)
    def __init__(self, name, age):    # 생성자 — 만들 때 이름·나이 넣기
        self.name = name              # 자기 이름 저장
        self.age = age                # 자기 나이 저장

    def bark(self):                   # 강아지가 할 수 있는 일: 짖기
        print(f"{self.name}: 멍멍!")

# 같은 틀로 강아지 두 마리 만들기
dog1 = Dog("뽀삐", 3)
dog2 = Dog("초코", 5)

dog1.bark()        # 뽀삐: 멍멍!
dog2.bark()        # 초코: 멍멍!
print(dog2.age)    # 5

# 함수/변수만으로 만들면? (참고 — 강아지가 많아지면 불편)
#   dog1_name = "뽀삐"; dog1_age = 3
#   dog2_name = "초코"; dog2_age = 5
#   def bark(name): print(f"{name}: 멍멍!")
#   -> 정보가 변수로 흩어지고, 짖을 때마다 이름을 밖에서 넘겨줘야 함

# 요약:
# - class 는 정보(속성) + 행동(메서드)을 한 묶음으로 관리하는 설계도
# - __init__ 은 객체 생성 시 자동 호출, self 는 각 객체 자기 자신을 가리킴
# - 같은 클래스로 객체를 여러 개 찍어내도 각자 자기 속성을 따로 가짐
